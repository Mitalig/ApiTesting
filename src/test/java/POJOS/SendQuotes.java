package POJOS;

public class SendQuotes {

	public String requestId;
	public String userEmail;
	public String userMobile;
	public String vertical;
	public String utmSource;
	public String utmMedium;
	public String action;
	public String url;

	public void setrequestId(String requestId) {

		this.requestId = requestId;
	}

	public void setuserEmail(String userEmail) {

		this.userEmail = userEmail;
	}

	public void setuserMobile(String userMobile) {

		this.userMobile = userMobile;

	}

	public void setvertical(String vertical) {

		this.vertical = vertical;
	}

	public void setutmSource(String utmSource) {

		this.utmSource = utmSource;
	}

	public void setutmMedium(String utmMedium) {

		this.utmMedium = utmMedium;

	}

	public void setaction(String action) {

		this.action = action;
	}

	public void seturl(String url) {

		this.url = url;

	}

	public String getrequestId(String requestId) {

		return requestId;
	}

	public String getuserEmail(String userEmail) {

		return userEmail;
	}

	public String getuserMobile(String userMobile) {

		return userMobile;

	}

	public String getvertical(String vertical) {

		return vertical;
	}

	public String getutmSource(String utmSource) {

		return utmSource;
	}

	public String getutmMedium(String utmMedium) {

		return utmMedium;

	}

	public String getaction(String action) {

		return action;
	}

	public String geturl(String url) {

		return url;

	}

}
